using System;

public class Layer
{
    public double width;
    public double paramA;
    public double paramB;
    public double paramC;
    public double paramTE;
    public double paramP;

    public Layer(double w, double pA, double pB, double pC, double pTE, double pP)
	{
        width = w;
        paramA = pA;
        paramB = pB;
        paramC = pC;
        paramTE = pTE;
        paramP = pP;
    }
}
